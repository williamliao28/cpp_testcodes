#include <stdio.h>
#include <stdlib.h>

int main(int argc, char **argv)
{
    int a=10, b=3;
	printf("a<b:  %d \n", a<b); 	/*  0:false 	*/
	printf("a>b:  %d \n", a>b);  	/*  1:true  	*/
	printf("a==b: %d \n", a==b); 	/*  0:false  	*/
	printf("a!=b: %d \n", a!=b); 	/*  1:true  	*/
	
	return 0;
}
